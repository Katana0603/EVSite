							</div>Die Ursprünge des Fremdenverkehrs gehen auf das Jahr 1816 zurück, in dem in Cuxhaven ein Seebad errichtet wurde. Mit jährlich über 3 Millionen Übernachtungen liegt Cuxhaven heute an der Spitze aller Kurorte in Deutschland. Es gelang in Cuxhaven, den Bereich des Fremdenverkehrs und den Kurbetrieb in geradezu idealer Weise räumlich scharf vom Industriegebiet zu trennen. Die Einrichtungen des Fremdenverkehrs, Sport- und Freizeitanlagen der Stadt sowie deren kulturelles Engagement garantieren einen sehr hohen Freizeit- und Wohnwert für Einwohner und Besucher Cuxhavens.<br>
							<br>
							Die Cuxhavener Fischwirtschaft kann ebenfalls auf eine lange Tradition zurückblicken. 1885 wurde der Fischereihafen errichtet. Cuxhaven ist neben Bremerhaven der bedeutendste Fischereistandort Deutschlands.<br>
							<br>
							Eine wichtige Rolle in Cuxhaven spielte auch das Militär. Die ersten Minensucheinheiten der deutschen Marine wurden in Cuxhaven aufgestellt. Aus strategischen Gründen verlegte man leider 1968 die schwimmenden Verbände nach Wilhelmshaven und Emden. <br>
							<br>
							Sehr wichtig für die Stadt Cuxhaven und für ihr Umland ist die Hafenwirtschaft – insbesondere das Mehrzweckumschlagsterminal der Firma Cuxport.  Das Hafengelände gehörte bis 1993 der Freien und Hansestadt Hamburg und erst nach langen und zähen Verhandlungen mit der Hansestadt Hamburger ging diese Fläche in den Besitz des Landes Niedersachsen über, deren Landesregierung mehrere Millionen Euro in den Bau der Hafenanlagen investierte. <br>
							<br>
							Durch seine günstige Lage an der Elbmündung ist Cuxhaven ideal für Short-Sea-Verkehre (z.B. Großbritannien, Skandinavien, Baltikum). Das Multipurpose Terminal  der Cuxport GmbH ist optimal geeignet für den Umschlag von z.B. Containern, RoRo, Automobillogistik und massenhafte Stückgüter. <br>
